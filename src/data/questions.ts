export function getQuestionById(id: string): QuizQuestion | undefined {
  return questions.find((q) => q.id === id);
}

export function getQuestionsByCategory(category: string): QuizQuestion[] {
  return questions.filter((q) => q.category === category);
}

export function getQuestionsBySubcategory(subcategory: string): QuizQuestion[] {
  return questions.filter((q) => q.subcategory === subcategory);
}

export function getRandomQuestions(count: number): QuizQuestion[] {
  const shuffled = [...questions].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, count);
}

function localizeQuestion(q: QuizQuestion, locale: Locale): QuizQuestion {
  if (locale === "en") return q;
  const localized = getLocalizedQuestion(q.id, q, locale);
  return { ...q, ...localized };
}

export function getLocalizedRandomQuestions(count: number, locale: Locale): QuizQuestion[] {
  return getRandomQuestions(count).map((q) => localizeQuestion(q, locale));
}

export function getLocalizedQuestions(locale: Locale): QuizQuestion[] {
  return questions.map((q) => localizeQuestion(q, locale));
}
