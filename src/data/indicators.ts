function localizeIndicator(indicator: Indicator, locale: Locale): Indicator {
  if (locale === "en") return indicator;
  const localized = getLocalizedIndicator(indicator.id, indicator, locale);
  return { ...indicator, ...localized };
}

export function getLocalizedIndicators(locale: Locale): Indicator[] {
  return indicators.map((i) => localizeIndicator(i, locale));
}

export function getLocalizedIndicatorsByCategory(category: string, locale: Locale): Indicator[] {
  return getIndicatorsByCategory(category).map((i) => localizeIndicator(i, locale));
}
